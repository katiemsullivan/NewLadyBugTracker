package Tables;

public class status {
	private 	int ID;
	private 	String description;
	private 	int iClass;
	private 	int iOrder;
	
	
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public int getiClass() {
		return iClass;
	}
	public void setiClass(int iClass) {
		this.iClass = iClass;
	}
	public int getiOrder() {
		return iOrder;
	}
	public void setiOrder(int iOrder) {
		this.iOrder = iOrder;
	}

}
